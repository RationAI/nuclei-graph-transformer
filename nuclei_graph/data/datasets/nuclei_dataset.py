import heapq
import random

import numpy as np
import pandas as pd
import torch
import torch.nn.functional as F
from degraph import build_spatial_graph
from einops import rearrange
from numpy.typing import NDArray
from scipy.spatial import KDTree
from torch import Tensor
from torch.utils.data import Dataset

from nuclei_graph.data import create_block_mask
from nuclei_graph.data.efd import elliptic_fourier_descriptors
from nuclei_graph.nuclei_graph_typing import Metadata, PredictSample, Sample


type Neighbor = tuple[int, float]  # (node_idx, edge_weight)
type AdjacencyGraph = list[list[Neighbor]]


class NucleiDataset(Dataset[Sample | PredictSample]):
    def __init__(
        self,
        df_metadata: pd.DataFrame,
        df_labels: pd.DataFrame,
        df_cam_indicators: pd.DataFrame | None = None,
        slides_positivity: dict[str, float] | None = None,
        alpha: float = 0.8,
        crop_size: int = 4096,
        k: int = 64,
        attn_block_size: int = 128,
        efd_order: int = 10,
        full_slide: bool = False,
        predict: bool = False,
    ) -> None:
        """Dataset for nuclei point clouds from whole-slide images.

        Args:
            df_metadata: Pandas DataFrame with columns: `slide_id` and `slide_nuclei_path` (if the predict mode is
                         set to `True` then also `slide_path`), where `slide_nuclei_path` points parquet files
                         containing nuclei data generated by `preprocessing/nuclei_segmentation.py`.
            df_labels: Pandas DataFrame with columns: `id` (str) and `label` (int).
            df_cam_indicators: Optional Pandas DataFrame containing finer annotation for positive slides with
                               columns `id` (str) and `cam_label_indicator` (bool).
            slides_positivity: Mapping from slide ID to fraction of positive nuclei for sampling during training.
            alpha: Weight between graph edge distance and Euclidean distance when selecting neighbors during graph construction.
            crop_size: Number of nuclei in a crop.
            k: Number of neighbors for sparse attention.
            attn_block_size: Block size for sparse attention; it must hold that `crop_size` mod `attn_block_size` is 0.
            efd_order: Order of the elliptic fourier descriptors used for nucleus shape representation.
            full_slide: Whether the dataset is used for full slide inference (no cropping).
            predict: Whether to return the metadata needed for prediction along with the data.
        """
        self.df_metadata = df_metadata
        self.df_labels = df_labels.set_index(["slide_id", "id"]).sort_index()
        self.df_cam_indicators = (
            df_cam_indicators.set_index(["slide_id", "id"]).sort_index()
            if df_cam_indicators is not None
            else None
        )
        self.slides_positivity = slides_positivity
        self.alpha = alpha
        self.crop_size = crop_size
        self.k = k
        self.attn_block_size = attn_block_size
        assert self.crop_size % self.attn_block_size == 0
        self.efd_order = efd_order
        self.full_slide = full_slide
        self.predict = predict

    def __len__(self) -> int:
        return len(self.df_metadata)

    def find_component(
        self,
        idx: int,
        k: int,
        graph: AdjacencyGraph,
        centroids: NDArray[np.float32],
        indices: NDArray[np.int64] | None = None,
    ) -> list[int]:
        """Grows a connected component of up to `k` nuclei starting from a seed index.

        Args:
            idx: seed nucleus index
            k: maximum number of nuclei to include in the component.
            graph: adjacency list of the nuclei graph.
            centroids: array of nucleus coordinates.
            indices: optional array of allowed nucleus indices

        Returns:
            list[int]: Indices of nuclei in the component.

        Taken from the Nuclei Foundational Model repository.
        """
        component_indices = []
        visited = np.zeros(len(centroids), dtype=bool)

        pq = []
        heapq.heappush(pq, (0, idx))
        start_point_coords = centroids[idx]

        while pq and len(component_indices) < k:
            _, current_idx = heapq.heappop(pq)
            if visited[current_idx]:
                continue

            visited[current_idx] = True
            component_indices.append(current_idx)

            for n_idx, edge_dist in graph[current_idx]:
                if not visited[n_idx] and (indices is None or n_idx in indices):
                    start_dist = np.linalg.norm(centroids[n_idx] - start_point_coords)
                    cost = self.alpha * edge_dist + (1 - self.alpha) * start_dist
                    heapq.heappush(pq, (cost, n_idx))
        return component_indices

    def add_rotation(self, positions: Tensor, psi_1: Tensor) -> Tensor:
        """Append the rotation angle as a third positional dimension."""
        angles = (psi_1 % torch.pi).view(-1, 1)
        return torch.cat([positions, angles], dim=1)

    def pad_to_block_size(
        self, x: torch.Tensor, pos: torch.Tensor, y: torch.Tensor, mask: torch.Tensor
    ) -> tuple[Tensor, Tensor, Tensor, Tensor]:
        """Pad tensors so that their first dimension is divisible by the attention block size."""
        n = x.size(0)
        remainder = n % self.attn_block_size

        if not self.full_slide or remainder == 0:
            return x, pos, y, mask

        pad_len = self.attn_block_size - remainder
        px, ppos, py, pmask = [F.pad(t, (0, 0, 0, pad_len)) for t in (x, pos, y, mask)]
        return px, ppos, py, pmask

    def get_inverse_perm(self, perm: Tensor) -> Tensor:
        assert perm.dim() == 1, "perm must be 1D"
        perm_inverse = torch.empty_like(perm)
        perm_inverse[perm] = torch.arange(perm.size(0), dtype=perm.dtype)
        assert torch.equal(
            perm[perm_inverse], torch.arange(perm.size(0), dtype=perm.dtype)
        )
        return perm_inverse

    def __getitem__(self, idx: int) -> Sample | PredictSample:
        nuclei = pd.read_parquet(self.df_metadata.iloc[idx].slide_nuclei_path)
        centroids = np.stack(nuclei["centroid"].tolist()).astype(np.float32)
        contours = rearrange(nuclei["polygon"].tolist(), "b (m c) -> b m c", c=2)
        efd = elliptic_fourier_descriptors(contours, self.efd_order)

        # save the rotation angle psi_1 before normalization
        # _, psi_1, _ = normalize_efd(nuclei_data["x"], return_angles=True)

        slide_id = self.df_metadata.iloc[idx].slide_id
        labels = torch.from_numpy(
            self.df_labels.loc[slide_id].reindex(nuclei["id"])["label"].values
        ).long()
        indicators = (
            torch.from_numpy(
                self.df_cam_indicators.loc[slide_id]
                .reindex(nuclei["id"])["cam_label_indicator"]
                .fillna(False)
                .values.astype(np.bool_)
            ).bool()
            if self.df_cam_indicators is not None
            else torch.ones(len(nuclei), dtype=torch.bool)
        )

        # compute crop indices
        if self.full_slide:
            crop_indices = torch.arange(len(efd))
        else:
            graph = build_spatial_graph(centroids)
            indicators_indices = (
                torch.nonzero(indicators.squeeze(-1), as_tuple=False)
                .squeeze(-1)
                .tolist()
            )  # ensure positive indicator is present in the crop
            assert indicators_indices
            seed = random.choice(indicators_indices)
            crop_indices = torch.tensor(
                self.find_component(seed, self.crop_size, graph, centroids),
                dtype=torch.long,
            )

        # permute for better data locality
        perm = KDTree(centroids[crop_indices], leafsize=self.attn_block_size).indices
        tree = KDTree(centroids[crop_indices][perm], leafsize=self.attn_block_size)
        perm = torch.from_numpy(perm).long()

        crop_efd = torch.from_numpy(efd[crop_indices][perm])
        crop_positions = torch.from_numpy(centroids[crop_indices][perm])
        crop_labels = labels[crop_indices][perm]
        crop_label_masks = indicators[crop_indices][perm]

        # crop_positions = self.add_rotation(crop_positions, angle[crop_indices][perm])

        crop_efd, crop_positions, crop_labels, crop_label_masks = (
            self.pad_to_block_size(
                crop_efd, crop_positions, crop_labels, crop_label_masks
            )
        )
        crop_block_mask = create_block_mask(
            kdtree=tree,
            points=crop_positions[
                :, :2
            ].numpy(),  # only pass the centroid 2D coordinates
            n_points_unpadded=len(crop_indices),
            k=self.k,
            block_size=self.attn_block_size,
        )
        item: Sample = {
            "x": crop_efd,  # (n, d)
            "pos": crop_positions,  # (n, 3)
            "y": crop_labels[crop_label_masks],  # (num_filtered,)
            "label_mask": crop_label_masks.unsqueeze(-1),  # (n, 1)
            "block_mask": crop_block_mask,  # BlockMask
        }

        if self.predict:
            perm_inverse = self.get_inverse_perm(perm)
            metadata: Metadata = {
                "slide_id": self.df_metadata.iloc[idx].slide_id,
                "slide_path": self.df_metadata.iloc[idx].slide_path,
                "slide_nuclei_path": self.df_metadata.iloc[idx].slide_nuclei_path,
                "nuclei_count": len(crop_indices),
                "perm_inverse": perm_inverse,
            }
            return item, metadata
        return item
